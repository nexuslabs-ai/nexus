/**
 * Generate tailwind.intellisense.css — an IntelliSense-ONLY Tailwind design
 * system that the app never imports at runtime.
 *
 * Why: the app runs the design system's nx: theme as a SEPARATE Tailwind
 * compilation (app/nexus.css) so its prefix can't clobber the app's own Tailwind
 * at build time. But the editor's Tailwind IntelliSense builds completions from
 * ONE design system per file and can't merge two entries
 * (tailwindcss-intellisense#665). Two `@import "tailwindcss"` with different
 * prefixes also collapse to a single prefix, so `@import "@acme/tailwind"` would
 * drop the app's own `bg-slate-*` completions.
 *
 * The fix: derive @acme/tailwind's `nexus.css` into ONE design system that keeps
 * the app default prefix and turns `nx` into a no-op VARIANT (not `prefix(nx)`):
 *   - drop `@import "tailwindcss" prefix(nx)` → `@import "tailwindcss"` + `@custom-variant nx (&)`
 *   - drop the `--*: initial` resets so the app's own `bg-slate-*` survive
 *   - inline the relative `@import "./*.css"` (variables + @utility sets) so the
 *     file is self-contained and the DS colors/typography/roles all complete
 * Everything else (scales, semantic colors) is kept, so `nx:bg-primary-background`,
 * `nx:typography-*`, `nx:rounded-lg`, `nx:shadow-lg` complete with DS values, and
 * `bg-slate-*`/`flex` still complete from the default theme. Never bundled, so
 * the runtime (and the shared-scale conflict) is untouched.
 *
 * Run:  node scripts/gen-intellisense-css.mjs   (also run by examples/scripts/setup.mjs)
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const appDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const twDir = path.join(appDir, 'node_modules', '@acme', 'tailwind');

let css = fs.readFileSync(path.join(twDir, 'nexus.css'), 'utf8');

// 1) app default prefix + nx as a no-op variant (instead of a global prefix flip)
const PREFIX_IMPORT = /@import ['"]tailwindcss['"] prefix\(nx\);/;
if (!PREFIX_IMPORT.test(css)) {
  throw new Error(
    "gen-intellisense-css: couldn't find `@import 'tailwindcss' prefix(nx);` in nexus.css. " +
      'The format drifted — the prefix flip would leak into IntelliSense; update this script.'
  );
}
css = css.replace(PREFIX_IMPORT, "@import 'tailwindcss';\n@custom-variant nx (&);");

// 2) drop the base-namespace resets so the app's own default utilities survive
css = css.replace(/\n\s*--[a-z-]+-\*: initial;/g, '');

// 3) inline the relative @imports (variables + @utility sets aren't package-exported)
css = css.replace(/@import ['"]\.\/([\w-]+\.css)['"];/g, (_m, file) => {
  const inlined = fs.readFileSync(path.join(twDir, file), 'utf8');
  return `/* ---- inlined ${file} ---- */\n${inlined}`;
});

const header = `/* GENERATED by scripts/gen-intellisense-css.mjs — do not edit by hand.
 * IntelliSense-ONLY combined design system (app default + DS nx: theme). NOT
 * imported by the app; see app/layout.tsx for the real runtime entries. Pointed
 * at via .vscode/settings.json so nx: AND bg-slate-* both autocomplete. */
`;

fs.writeFileSync(path.join(appDir, 'tailwind.intellisense.css'), header + css);
console.log('wrote tailwind.intellisense.css (self-contained combined design system)');
