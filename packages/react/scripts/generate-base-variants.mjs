/*
 * generate-base-variants.mjs — generates per-base Storybook variant stories.
 *
 * For every component listed in base-variants.config.json this emits one story
 * that renders the component's existing showcase across all bases × themes in a
 * single grid, plus one CSS file scoping each base's semantic tokens to a
 * `[data-nexus-base]` subtree. Output lands in src/components/__generated__/
 * (gitignored) and is regenerated on each storybook / test run.
 *
 * The color resolver/flattener below re-implements the color subset of
 * packages/core/scripts/utils.js (refToVar ≈ resolveReference,
 * flattenColorTokens ≈ extractTokens + processSemanticTokens). It is a copy,
 * not an import: core/scripts is not in @nexus/core's exports/dist, and reading
 * the committed semantic JSON directly keeps this generator free of a token
 * build-order dependency (output is verified byte-identical to dist/modular).
 *
 * See .claude/rules/storybook.md § Per-base variant generation.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CONFIG_PATH = path.join(__dirname, 'base-variants.config.json');
const SEMANTIC_DIR = path.join(__dirname, '../../core/tokens/semantic');
const PRIMITIVES_PATH = path.join(__dirname, '../../core/tokens/primitives/color.json');
const COMPONENTS_DIR = path.join(__dirname, '../src/components');
const OUT_DIR = path.join(__dirname, '../src/components/__generated__');
const KNOWN_SOURCE_DIRS = new Set(['ui', 'primitives']);

function readJson(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Required file missing: ${filePath}`);
  }
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

/**
 * Flatten the primitive color tree into the set of valid CSS names
 * (`slate-200`, `white`, `slate-a200`) — every resolvable target a `{…}`
 * semantic reference can point at. Built once; `refToVar` validates against it.
 */
function collectPrimitiveNames(node, prefix = []) {
  const names = new Set();
  for (const [key, val] of Object.entries(node)) {
    if (key.startsWith('$') || val === null || typeof val !== 'object') {
      continue;
    }
    if (val.$value !== undefined) {
      names.add([...prefix, key].join('-'));
      continue;
    }
    for (const name of collectPrimitiveNames(val, [...prefix, key])) {
      names.add(name);
    }
  }
  return names;
}

const PRIMITIVE_NAMES = collectPrimitiveNames(readJson(PRIMITIVES_PATH));

/**
 * Convert a DTCG reference (`{slate.200}`, `{white}`, `{slate.a200}`) to the
 * prefixed primitive CSS variable the Tailwind utilities consume.
 * Dots become dashes; everything else is preserved.
 */
function refToVar(value) {
  if (typeof value !== 'string' || !value.startsWith('{') || !value.endsWith('}')) {
    throw new Error(`Expected a {…} token reference, got: ${JSON.stringify(value)}`);
  }
  const ref = value.slice(1, -1).replace(/\./g, '-');
  if (!PRIMITIVE_NAMES.has(ref)) {
    throw new Error(
      `Unresolvable token reference ${value} → --nx-color-${ref} (not a primitive in color.json)`
    );
  }
  return `var(--nx-color-${ref})`;
}

/**
 * Recursively flatten a DTCG token tree into [{ cssVar, value }].
 * Nested groups join with `-`; `$`-prefixed metadata keys are skipped.
 */
function flattenColorTokens(node, prefix = []) {
  const out = [];
  for (const [key, val] of Object.entries(node)) {
    if (key.startsWith('$') || val === null || typeof val !== 'object') {
      continue;
    }
    // A leaf needs both $value and $type (mirrors core's extractTokens). Only
    // color tokens take the --nx-color- prefix; gating on $type keeps a future
    // non-color semantic alias from being mis-prefixed and emitted here.
    if (val.$value !== undefined && val.$type !== undefined) {
      if (val.$type === 'color') {
        out.push({
          cssVar: `--nx-color-${[...prefix, key].join('-')}`,
          value: refToVar(val.$value),
        });
      }
      continue;
    }
    out.push(...flattenColorTokens(val, [...prefix, key]));
  }
  return out;
}

/**
 * Concatenate base + brand tokens for one base × theme. Brand owns only
 * primary/secondary, so the two sets are disjoint in practice; brand is
 * emitted last so the CSS cascade favours it on any future overlap.
 */
function tokensFor(base, brand, theme) {
  const baseTokens = flattenColorTokens(
    readJson(path.join(SEMANTIC_DIR, `base-${base}-${theme}.json`))
  );
  const brandTokens = flattenColorTokens(
    readJson(path.join(SEMANTIC_DIR, `brands-${brand}-${theme}.json`))
  );
  return [...baseTokens, ...brandTokens];
}

function buildCss(config) {
  let css = '/* AUTO-GENERATED by scripts/generate-base-variants.mjs — DO NOT EDIT. */\n';
  css += '/* Semantic tokens scoped per base × theme for cross-base variant stories. */\n\n';
  for (const base of config.bases) {
    for (const theme of config.themes) {
      const selector =
        theme === 'dark'
          ? `[data-nexus-base="${base}"].dark`
          : `[data-nexus-base="${base}"]`;
      const decls = tokensFor(base, config.brand, theme)
        .map((t) => `  ${t.cssVar}: ${t.value};`)
        .join('\n');
      css += `${selector} {\n${decls}\n}\n\n`;
    }
  }
  return css;
}

function sourceDirFor(component) {
  const dir = component.sourceDir ?? 'ui';
  if (!KNOWN_SOURCE_DIRS.has(dir)) {
    throw new Error(
      `Unknown sourceDir "${dir}" for "${component.name}". Expected one of: ${[
        ...KNOWN_SOURCE_DIRS,
      ].join(', ')}`
    );
  }
  return dir;
}

function storiesImportPath(component) {
  return `../${sourceDirFor(component)}/${component.name}.stories`;
}

function storiesFilePath(component) {
  return path.join(COMPONENTS_DIR, sourceDirFor(component), `${component.name}.stories.tsx`);
}

function buildStory(component, config) {
  const bases = JSON.stringify(config.bases);
  const themes = JSON.stringify(config.themes);
  const storiesPath = storiesImportPath(component);
  return `/* AUTO-GENERATED by scripts/generate-base-variants.mjs — DO NOT EDIT. */
/* Source: ${storiesPath} (${component.showcase}). */
import type { Meta, StoryObj } from '@storybook/react-vite';
import type { ReactNode } from 'react';

import componentMeta, { ${component.showcase} as showcase } from '${storiesPath}';

import './base-variants.css';

const BASES: string[] = ${bases};
const THEMES: string[] = ${themes};

type AnyRender = (args: unknown, context: unknown) => ReactNode;
type AnyDecorator = (story: () => ReactNode, context: unknown) => ReactNode;

function toDecorators(d: unknown): AnyDecorator[] {
  if (!d) return [];
  return (Array.isArray(d) ? d : [d]) as AnyDecorator[];
}

function renderShowcase(): ReactNode {
  const render = showcase.render as unknown as AnyRender | undefined;
  if (!render) return null;
  // Apply the component's own decorators (meta then story), outermost first —
  // e.g. Tooltip's TooltipProvider. The global preview decorator (dark /
  // centering wrapper) is intentionally skipped so cells stay self-contained.
  // Showcases are render-based and ignore args/context, so both are bare {}.
  const decorators = [
    ...toDecorators(componentMeta.decorators),
    ...toDecorators(showcase.decorators),
  ];
  return decorators.reduceRight<() => ReactNode>(
    (inner, decorator) => () => decorator(inner, {}),
    () => render({}, {})
  )();
}

function BaseCell({ base, theme }: { base: string; theme: string }) {
  return (
    <div
      data-nexus-base={base}
      className={theme === 'dark' ? 'dark' : undefined}
      style={{
        display: 'flex',
        flexDirection: 'column',
        gap: '0.75rem',
        padding: '1rem',
        borderRadius: '0.75rem',
        border: '1px solid var(--nx-color-border-default)',
        backgroundColor: 'var(--nx-color-background)',
        color: 'var(--nx-color-foreground)',
      }}
    >
      <span
        style={{
          fontSize: '0.6875rem',
          fontWeight: 600,
          textTransform: 'uppercase',
          letterSpacing: '0.04em',
          color: 'var(--nx-color-muted-foreground)',
        }}
      >
        {base}
      </span>
      {renderShowcase()}
    </div>
  );
}

function BaseMatrix() {
  return (
    <div style={{ display: 'flex', flexDirection: 'column', gap: '2rem', padding: '1.5rem' }}>
      {THEMES.map((theme) => (
        <div key={theme} style={{ display: 'flex', flexDirection: 'column', gap: '0.5rem' }}>
          <div
            style={{
              fontSize: '0.75rem',
              fontWeight: 700,
              textTransform: 'uppercase',
              letterSpacing: '0.06em',
              color: 'var(--nx-color-foreground)',
            }}
          >
            {theme}
          </div>
          <div
            style={{
              display: 'grid',
              gridTemplateColumns: \`repeat(\${BASES.length}, minmax(15rem, 1fr))\`,
              gap: '1rem',
            }}
          >
            {BASES.map((base) => (
              <BaseCell key={base} base={base} theme={theme} />
            ))}
          </div>
        </div>
      ))}
    </div>
  );
}

const meta: Meta<typeof BaseMatrix> = {
  title: 'Base Variants/${component.name}',
  component: BaseMatrix,
  parameters: { layout: 'fullscreen', a11y: { test: 'off' } },
  tags: ['!autodocs'],
};

export default meta;

type Story = StoryObj<typeof BaseMatrix>;

export const AllBases: Story = {};
`;
}

function escapeRegExp(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

/**
 * Fail fast (before templating) if the canonical stories file is missing, its
 * named showcase export is absent, or that export is args-only — each otherwise
 * yields a valid `.tsx` that breaks (or renders empty cells) only at the
 * Storybook/vitest build. The generator reuses `showcase.render()` directly, so
 * a render-based export is a hard precondition.
 */
function assertShowcaseExists(component) {
  const file = storiesFilePath(component);
  if (!fs.existsSync(file)) {
    throw new Error(`Stories file missing for "${component.name}": ${file}`);
  }
  const src = fs.readFileSync(file, 'utf8');
  // Anchor to the start of a line (multiline flag) so a commented or stringified
  // mention can't satisfy the guard; escape the config value before interpolating.
  const exportMatch = new RegExp(`^export const ${escapeRegExp(component.showcase)}\\b`, 'm').exec(
    src
  );
  if (!exportMatch) {
    throw new Error(
      `Showcase export "${component.showcase}" not found in ${path.relative(COMPONENTS_DIR, file)}`
    );
  }
  // Require a `render:` object key within the export's block (up to the next
  // top-level export) so an args-only story fails here rather than rendering
  // empty cells. Match `render:` either at a line start (the indented-key case,
  // robust to intervening comments/keys) or right after a `{`/`,` (same-line
  // case). This rejects a `render:` inside a string value in every realistic
  // shape; the lone residual — one at column 0 inside a multiline template
  // literal — is contrived and would surface in the render/smoke test.
  const rest = src.slice(exportMatch.index);
  const nextExport = rest.slice(exportMatch[0].length).search(/^export /m);
  const block = nextExport === -1 ? rest : rest.slice(0, exportMatch[0].length + nextExport);
  if (!/(?:^|[{,])\s*render\s*:/m.test(block)) {
    throw new Error(
      `Showcase "${component.showcase}" in ${path.relative(COMPONENTS_DIR, file)} is args-only; ` +
        `base-variant generation needs a render-based story`
    );
  }
}

function main() {
  console.log('🎨 Generating per-base Storybook variant stories…\n');
  const config = readJson(CONFIG_PATH);

  fs.rmSync(OUT_DIR, { recursive: true, force: true });
  fs.mkdirSync(OUT_DIR, { recursive: true });

  fs.writeFileSync(path.join(OUT_DIR, 'base-variants.css'), buildCss(config));
  console.log(
    `  ✓ base-variants.css (${config.bases.length} bases × ${config.themes.length} themes)`
  );

  for (const component of config.components) {
    assertShowcaseExists(component);
    const fileName = `${component.name}.base-variants.stories.tsx`;
    fs.writeFileSync(path.join(OUT_DIR, fileName), buildStory(component, config));
    console.log(`  ✓ ${fileName} (${component.showcase})`);
  }

  console.log(
    `\n✨ Generated ${config.components.length} base-variant stories in src/components/__generated__/`
  );
}

main();
