/*
 * generate-base-variants.mjs — generates per-base Storybook variant stories.
 *
 * For every component listed in base-variants.config.json this emits one story
 * that renders the component's existing showcase across all bases × themes in a
 * single grid, plus one CSS file scoping each base's semantic tokens to a
 * `[data-nexus-base]` subtree. Output lands in src/components/__generated__/
 * (gitignored) and is regenerated on each storybook / test run.
 *
 * See .claude/rules/storybook.md § Per-base variant generation.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CONFIG_PATH = path.join(__dirname, 'base-variants.config.json');
const SEMANTIC_DIR = path.join(__dirname, '../../core/tokens/semantic');
const OUT_DIR = path.join(__dirname, '../src/components/__generated__');

function readJson(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Required file missing: ${filePath}`);
  }
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

/**
 * Convert a DTCG reference (`{slate.200}`, `{white}`, `{slate.a200}`) to the
 * prefixed primitive CSS variable the Tailwind utilities consume.
 * Dots become dashes; everything else is preserved.
 */
function refToVar(value) {
  if (typeof value !== 'string' || !value.startsWith('{') || !value.endsWith('}')) {
    console.warn(`  ⚠ non-reference semantic value passed through verbatim: ${value}`);
    return value;
  }
  const ref = value.slice(1, -1).replace(/\./g, '-');
  return `var(--nx-color-${ref})`;
}

/**
 * Recursively flatten a DTCG token tree into [{ cssVar, value }].
 * Nested groups join with `-`; `$`-prefixed metadata keys are skipped.
 */
function flattenColorTokens(node, prefix = []) {
  const out = [];
  for (const [key, val] of Object.entries(node)) {
    if (key.startsWith('$') || val === null || typeof val !== 'object') {
      continue;
    }
    if (val.$value !== undefined) {
      out.push({
        cssVar: `--nx-color-${[...prefix, key].join('-')}`,
        value: refToVar(val.$value),
      });
      continue;
    }
    out.push(...flattenColorTokens(val, [...prefix, key]));
  }
  return out;
}

/**
 * Merge base + brand tokens for one base × theme. Brand wins on collision
 * (disjoint in practice — brand owns primary/secondary only).
 */
function tokensFor(base, brand, theme) {
  const baseTokens = flattenColorTokens(
    readJson(path.join(SEMANTIC_DIR, `base-${base}-${theme}.json`))
  );
  const brandTokens = flattenColorTokens(
    readJson(path.join(SEMANTIC_DIR, `brands-${brand}-${theme}.json`))
  );
  const merged = new Map();
  for (const t of [...baseTokens, ...brandTokens]) {
    merged.set(t.cssVar, t.value);
  }
  return [...merged].map(([cssVar, value]) => ({ cssVar, value }));
}

function buildCss(config) {
  let css = '/* AUTO-GENERATED by scripts/generate-base-variants.mjs — DO NOT EDIT. */\n';
  css += '/* Semantic tokens scoped per base × theme for cross-base variant stories. */\n\n';
  for (const base of config.bases) {
    for (const theme of config.themes) {
      const selector =
        theme === 'dark'
          ? `[data-nexus-base="${base}"].dark`
          : `[data-nexus-base="${base}"]`;
      const decls = tokensFor(base, config.brand, theme)
        .map((t) => `  ${t.cssVar}: ${t.value};`)
        .join('\n');
      css += `${selector} {\n${decls}\n}\n\n`;
    }
  }
  return css;
}

function buildStory(component, config) {
  const bases = JSON.stringify(config.bases);
  const themes = JSON.stringify(config.themes);
  return `/* AUTO-GENERATED by scripts/generate-base-variants.mjs — DO NOT EDIT. */
/* Source: ../ui/${component.storiesFile} (${component.showcase}). */
import type { Meta, StoryObj } from '@storybook/react-vite';
import type { ReactNode } from 'react';

import componentMeta, { ${component.showcase} as showcase } from '../ui/${component.storiesFile}';

import './base-variants.css';

const BASES: string[] = ${bases};
const THEMES: string[] = ${themes};

type AnyRender = (args: unknown, context: unknown) => ReactNode;

function renderShowcase(): ReactNode {
  const render = showcase.render as unknown as AnyRender | undefined;
  return render?.({ ...componentMeta.args, ...showcase.args }, {});
}

function BaseCell({ base, theme }: { base: string; theme: string }) {
  return (
    <div
      data-nexus-base={base}
      className={theme === 'dark' ? 'dark' : undefined}
      style={{
        display: 'flex',
        flexDirection: 'column',
        gap: '0.75rem',
        padding: '1rem',
        borderRadius: '0.75rem',
        border: '1px solid var(--nx-color-border-default)',
        backgroundColor: 'var(--nx-color-background)',
        color: 'var(--nx-color-foreground)',
      }}
    >
      <span
        style={{
          fontSize: '0.6875rem',
          fontWeight: 600,
          textTransform: 'uppercase',
          letterSpacing: '0.04em',
          color: 'var(--nx-color-muted-foreground)',
        }}
      >
        {base}
      </span>
      {renderShowcase()}
    </div>
  );
}

function BaseMatrix() {
  return (
    <div style={{ display: 'flex', flexDirection: 'column', gap: '2rem', padding: '1.5rem' }}>
      {THEMES.map((theme) => (
        <div key={theme} style={{ display: 'flex', flexDirection: 'column', gap: '0.5rem' }}>
          <div
            style={{
              fontSize: '0.75rem',
              fontWeight: 700,
              textTransform: 'uppercase',
              letterSpacing: '0.06em',
              color: 'var(--nx-color-foreground)',
            }}
          >
            {theme}
          </div>
          <div
            style={{
              display: 'grid',
              gridTemplateColumns: \`repeat(\${BASES.length}, minmax(15rem, 1fr))\`,
              gap: '1rem',
            }}
          >
            {BASES.map((base) => (
              <BaseCell key={base} base={base} theme={theme} />
            ))}
          </div>
        </div>
      ))}
    </div>
  );
}

const meta: Meta<typeof BaseMatrix> = {
  title: 'Base Variants/${component.name}',
  component: BaseMatrix,
  parameters: { layout: 'fullscreen', a11y: { test: 'off' } },
  tags: ['!autodocs'],
};

export default meta;

type Story = StoryObj<typeof BaseMatrix>;

export const AllBases: Story = {};
`;
}

function main() {
  console.log('🎨 Generating per-base Storybook variant stories…\n');
  const config = readJson(CONFIG_PATH);

  fs.rmSync(OUT_DIR, { recursive: true, force: true });
  fs.mkdirSync(OUT_DIR, { recursive: true });

  fs.writeFileSync(path.join(OUT_DIR, 'base-variants.css'), buildCss(config));
  console.log(
    `  ✓ base-variants.css (${config.bases.length} bases × ${config.themes.length} themes)`
  );

  for (const component of config.components) {
    const fileName = `${component.name}.base-variants.stories.tsx`;
    fs.writeFileSync(path.join(OUT_DIR, fileName), buildStory(component, config));
    console.log(`  ✓ ${fileName} (${component.showcase})`);
  }

  console.log(
    `\n✨ Generated ${config.components.length} base-variant stories in src/components/__generated__/`
  );
}

main();
